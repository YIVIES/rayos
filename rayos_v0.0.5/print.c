void putc(const char ch)
{
    uchar *ptr = (uchar *)vga_map;

    if (ch == '\0')
        return;
    if (ch == '\t') {
        putc(' ');
        putc(' ');
        putc(' ');
        putc(' ');
        return;
    }
    if (ch == '\n') {
        vga_pos += 80;
        vga_pos -= vga_pos % 80;
        if (vga_pos >= 2000) {
            vga_pos = 1920;
            vga_rollup();
            return;
        }
        set_cursor((ushort)vga_pos);
        vga_refresh();
        return;
    }
    if (ch == '\b') {
        if (vga_pos <= vga_back_limit)
            return;
        vga_pos--;
        putc(' ');
        vga_pos--;
        set_cursor((ushort)vga_pos);
        return;
    }
    *(ptr + vga_pos) = ch;
    vga_pos++;
    if (vga_pos >= 2000) {
        vga_pos = 1920;
        vga_rollup();
        return;
    }
    set_cursor((ushort)vga_pos);
    vga_refresh();
}

int puts(const char *str)
{
    int i;
    int len;

    for (len = 0; str[len] != '\0'; len++);
    for (i = 0; i < len; i++) {
        putc(str[i]);
    }
    vga_back_limit = vga_pos;
    return len;
}

void vga_refresh(void)
{
    int i;
    uchar *ptr = (uchar *)vga_map;

    for (i = 0; i < 2000; i++) {
        *(vga + (i << 1)) = *(ptr++);
    }
}

void vga_rollup(void)
{
    int i, j;

    for (i = 0; i < 24; i++) {
        for (j = 0; j < 80; j++) {
            vga_map[i][j] = vga_map[i + 1][j];
        }
    }

    for (i = 0; i < 80; i++) {
        vga_map[24][i] = ' ';
    }

    vga_refresh();
    set_cursor((ushort)vga_pos);
}